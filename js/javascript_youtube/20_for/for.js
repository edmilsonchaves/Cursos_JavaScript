/*
    ESTRUTURA FOR

* O FOR tem uma sintaxe mais complexa, porém mais optado pela maioria dos programadores;
* Apesar de parecer mais difícil, como a instrução fica todo em uma linha proporciona melhor controle

*/

for(let num = 0; num <= 10; num++ ) {
    
    console.log(`Valor de num ${num}`)
}

// Podemos fazer também com arrays

let nomes = ['arthur', 'anna clara', 'theo', 'edmilson']

for(let i = 0; i < nomes.length; i++) {

    console.log(nomes[i])
}

